package rsstats.common.container;

import net.minecraft.entity.player.EntityPlayer;
import net.minecraft.entity.player.InventoryPlayer;
import net.minecraft.inventory.Container;
import net.minecraft.inventory.Slot;
import net.minecraft.tileentity.TileEntity;

/**
 *
 * @author rares
 */
public class MainMenuContainer extends Container {

    public MainMenuContainer() {
        
    }
    public MainMenuContainer(InventoryPlayer inventoryPlayer, TileEntity te) {

        // Player Inventory, Slot 9-35, Slot IDs 9-35
        for (int y = 0; y < 3; ++y) {
            for (int x = 0; x < 9; ++x) {
                this.addSlotToContainer(new Slot(inventoryPlayer, x + y * 9 + 9, 8 + x * 18, 84 + y * 18));
            }
        }

    }
    
    @Override
    public boolean canInteractWith(EntityPlayer player) {
        return true;
    }
    
}
